/**
 * Get category preset data by name
 */
function getCategoryPreset(categoryName) {
  return categoryPresets[categoryName] || null;
}

/**
 * Get all category preset names
 */
function getCategoryPresetNames() {
  return Object.keys(categoryPresets);
}

/**
 * Auto-fill category metadata from presets
 */
function autoFillCategoryMetadata(categoryName) {
  const preset = getCategoryPreset(categoryName);
  if (preset) {
    return {
      shortDescription: preset.shortDesc,
      seoTitle: preset.seoTitle,
      seoDescription: preset.seoDesc
    };
  }
  return null;
}

/**
 * Load preset into form fields
 */
function loadPresetIntoForm(categoryName, formPrefix = '') {
  const preset = getCategoryPreset(categoryName);
  if (preset) {
    const shortDescField = document.getElementById(formPrefix + 'short_description') || 
                          document.getElementById(formPrefix + 'ShortDescription');
    const seoTitleField = document.getElementById(formPrefix + 'seo_title') || 
                         document.getElementById(formPrefix + 'SeoTitle');
    const seoDescField = document.getElementById(formPrefix + 'seo_description') || 
                        document.getElementById(formPrefix + 'SeoDescription');
    
    if (shortDescField) shortDescField.value = preset.shortDesc;
    if (seoTitleField) seoTitleField.value = preset.seoTitle;
    if (seoDescField) seoDescField.value = preset.seoDesc;
    
    return true;
  }
  return false;
}

/**
 * Check if a category has a preset
 */
function hasCategoryPreset(categoryName) {
  return categoryName in categoryPresets;
}

/**
 * Get all available category names
 */
function getAvailableCategoryNames() {
  return Object.keys(categoryPresets);
}

